import { format } from "shared/sql";
import type { SchemaFormatConfig } from "shared/types/datasource";
import type { MetricType } from "shared/types/metric";
import type { SqlDialect } from "shared/types/sql";

export function getAutoGeneratedMetricSqlQuery(
  dialect: SqlDialect,
  schemaFormatConfig: SchemaFormatConfig,
  eventName: string,
  hasUserId: boolean,
  type: MetricType,
  schema?: string,
): string {
  const {
    timestampColumn,
    userIdColumn,
    anonymousIdColumn,
    getTrackedEventTablePath,
    getEventFilterWhereClause,
    getDateLimitClause,
  } = schemaFormatConfig;

  const sqlQuery = `
      SELECT
        ${hasUserId ? `${userIdColumn} as user_id, ` : ""}
        ${anonymousIdColumn} as anonymous_id,
        ${timestampColumn} as timestamp
        ${type === "count" ? `, 1 as value` : ""}
        FROM ${getTrackedEventTablePath({ eventName, schema })}
        WHERE ${getDateLimitClause()} ${
          getEventFilterWhereClause(eventName).length
            ? ` AND ${getEventFilterWhereClause(eventName)}`
            : ""
        }
`;
  return format(sqlQuery, dialect.formatDialect);
}
